// Relational Operators

/*
0 = false
1 = true
*/

#include <stdio.h>
int main(){
    int num1 = 5;
    int num2 = 8;

    printf("%d\n", num1==num2);
    printf("%d\n", num1>num2);
    printf("%d\n", num1>=num2);
    printf("%d\n", num1<num2);
    printf("%d\n", num1<=num2);
    printf("%d\n", num1!=num2);


}